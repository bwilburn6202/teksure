#!/usr/bin/env node
/**
 * continuous-content-loop.mjs — runs hourly via GitHub Actions and produces
 * one piece of content per cycle, rotating across:
 *   0: new guide (delegates to creative-loop.mjs)
 *   1: freshness update (stamps lastVerifiedAt on aged guides)
 *   2: scam alert guide (from SCAM_ALERTS seed)
 *   3: weekly tip guide (from WEEKLY_TIPS seed)
 *
 * Modifies files in place. The workflow handles commit/push/PR.
 * Exits 0 on no-op (nothing new to add) so the workflow can skip the PR.
 *
 * State: .claude/continuous-content-state.json
 *   { cycleCount, lastAction, lastRun, usedAlertSlugs[], usedTipSlugs[],
 *     verifiedSlugs[] }
 *
 * Flags: --dry-run, --action=<guide|freshness|alert|tip>
 */
import fs from 'node:fs';
import path from 'node:path';
import { spawnSync } from 'node:child_process';
import { fileURLToPath } from 'node:url';
import { SCAM_ALERTS, WEEKLY_TIPS } from './continuous-content/seeds.mjs';

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const ROOT = path.resolve(__dirname, '..');
const DATA_DIR = path.join(ROOT, 'src', 'data');
const STATE_FILE = path.join(ROOT, '.claude', 'continuous-content-state.json');
const GUIDES_INDEX = path.join(DATA_DIR, 'guides.ts');

const ACTIONS = ['guide', 'freshness', 'alert', 'tip'];
const FRESHNESS_BATCH_SIZE = 12;

const args = parseArgs(process.argv.slice(2));
const DRY = args.has('dry-run');
const FORCED_ACTION = args.get('action');

function parseArgs(argv) {
  const map = new Map();
  for (const raw of argv) {
    const [k, v] = raw.includes('=') ? raw.split('=') : [raw, 'true'];
    map.set(k.replace(/^--/, ''), v);
  }
  return map;
}

function log(...m) { console.log('[continuous-content]', ...m); }

function readState() {
  try {
    return JSON.parse(fs.readFileSync(STATE_FILE, 'utf8'));
  } catch {
    return {
      version: '1.0',
      cycleCount: 0,
      lastAction: null,
      lastRun: null,
      usedAlertSlugs: [],
      usedTipSlugs: [],
      verifiedSlugs: [],
    };
  }
}

function writeState(state) {
  if (DRY) return;
  fs.mkdirSync(path.dirname(STATE_FILE), { recursive: true });
  fs.writeFileSync(STATE_FILE, JSON.stringify(state, null, 2));
}

function tsEscape(s) {
  return String(s).replace(/\\/g, '\\\\').replace(/'/g, "\\'");
}
function tsTplEscape(s) {
  return String(s).replace(/\\/g, '\\\\').replace(/`/g, '\\`').replace(/\$\{/g, '\\${');
}

function collectExistingSlugs() {
  const slugs = new Set();
  const files = fs.readdirSync(DATA_DIR)
    .filter((f) => f.startsWith('guides') && f.endsWith('.ts'));
  const re = /slug:\s*['"`]([^'"`]+)['"`]/g;
  for (const f of files) {
    const c = fs.readFileSync(path.join(DATA_DIR, f), 'utf8');
    let m;
    while ((m = re.exec(c)) !== null) slugs.add(m[1]);
  }
  return slugs;
}

function nextBatchNumber() {
  let max = 0;
  for (const f of fs.readdirSync(DATA_DIR)) {
    const m = f.match(/^guides-batch-(\d+)\.ts$/);
    if (m) max = Math.max(max, Number(m[1]));
  }
  return max + 1;
}

function renderStep(s, indent = '      ') {
  const out = [`${indent}{`, `${indent}  title: '${tsEscape(s.title)}',`, `${indent}  content: '${tsEscape(s.content)}',`];
  if (s.tip) out.push(`${indent}  tip: '${tsEscape(s.tip)}',`);
  if (s.warning) out.push(`${indent}  warning: '${tsEscape(s.warning)}',`);
  out.push(`${indent}},`);
  return out.join('\n');
}

function renderGuide(t, today) {
  const tags = t.tags.map((x) => `'${tsEscape(x)}'`).join(', ');
  const steps = (t.steps || []).map((s) => renderStep(s)).join('\n');
  return `  {
    slug: '${tsEscape(t.slug)}',
    title: '${tsEscape(t.title)}',
    excerpt: '${tsEscape(t.excerpt)}',
    category: '${tsEscape(t.category)}',
    tags: [${tags}],
    readTime: '${tsEscape(t.readTime || '4 min')}',
    thumbnailEmoji: '${tsEscape(t.emoji || '📘')}',
    publishedAt: '${today}',
    lastVerifiedAt: '${today}',
    difficulty: '${tsEscape(t.difficulty || 'Beginner')}',
    body: \`${tsTplEscape(t.body)}\`,
    steps: [
${steps}
    ],
  },`;
}

function renderBatchFile(batchNumber, label, items, today) {
  const blocks = items.map((t) => renderGuide(t, today)).join('\n\n');
  return `// guides-batch-${batchNumber} — Auto-generated by continuous-content-loop on ${today}
// Type: ${label}
import type { Guide } from './guides';

export const guidesBatch${batchNumber}: Guide[] = [
${blocks}
];
`;
}

function wireUpBatch(indexContent, batchNumber) {
  const importLine = `import { guidesBatch${batchNumber} } from './guides-batch-${batchNumber}';`;
  const spread = `...guidesBatch${batchNumber}`;
  const importRe = /import \{ guidesBatch(\d+) \} from '\.\/guides-batch-\1';/g;
  let lastEnd = -1;
  let m;
  while ((m = importRe.exec(indexContent)) !== null) lastEnd = m.index + m[0].length;
  if (lastEnd === -1) throw new Error('No guidesBatchN imports found in guides.ts');
  if (indexContent.includes(importLine)) return indexContent;
  let next = indexContent.slice(0, lastEnd) + '\n' + importLine + indexContent.slice(lastEnd);
  const arrRe = /(const allGuides: Guide\[\] = \[[^\]]*?)(\];)/s;
  if (!arrRe.test(next)) throw new Error('allGuides array not found');
  if (!next.includes(spread)) {
    next = next.replace(arrRe, (_full, before, end) => {
      const trimmed = before.replace(/,\s*$/, '');
      return `${trimmed}, ${spread}${end}`;
    });
  }
  return next;
}

function run(cmd, opts = {}) {
  const [bin, ...rest] = Array.isArray(cmd) ? cmd : cmd.split(' ');
  const r = spawnSync(bin, rest, { cwd: ROOT, encoding: 'utf8', timeout: opts.timeout ?? 180_000 });
  return { ok: r.status === 0, status: r.status ?? 1, stdout: (r.stdout ?? '').trim(), stderr: (r.stderr ?? '').trim() };
}

function writeSeedBatch(label, items, state, usedKey) {
  const existing = collectExistingSlugs();
  const used = new Set(state[usedKey] ?? []);
  const pick = items.find((t) => !used.has(t.slug) && !existing.has(t.slug));
  if (!pick) {
    log(`No fresh ${label} left in seed library.`);
    return { noop: true };
  }
  const today = new Date().toISOString().slice(0, 10);
  const batchNumber = nextBatchNumber();
  const file = path.join(DATA_DIR, `guides-batch-${batchNumber}.ts`);
  const content = renderBatchFile(batchNumber, label, [pick], today);
  log(`Writing ${label}: ${pick.slug}`);
  if (DRY) return { noop: false, dry: true, picked: pick };
  fs.writeFileSync(file, content);
  const idx = fs.readFileSync(GUIDES_INDEX, 'utf8');
  fs.writeFileSync(GUIDES_INDEX, wireUpBatch(idx, batchNumber));
  const slugCheck = run('node scripts/validate-slugs.mjs');
  if (!slugCheck.ok) {
    log('Slug validation failed; rolling back.');
    log(slugCheck.stdout, slugCheck.stderr);
    fs.unlinkSync(file);
    fs.writeFileSync(GUIDES_INDEX, idx);
    return { noop: true, error: 'slug-validation' };
  }
  used.add(pick.slug);
  state[usedKey] = [...used];
  return { noop: false, picked: pick, batchNumber, file };
}

/**
 * Stamp lastVerifiedAt on guides in src/data/guides.ts that don't have one yet.
 * Only edits the core guides.ts (batches are usually freshly auto-generated).
 */
function freshnessPass(state) {
  const today = new Date().toISOString().slice(0, 10);
  const src = fs.readFileSync(GUIDES_INDEX, 'utf8');
  const verified = new Set(state.verifiedSlugs ?? []);
  // Match each guide object: capture slug + the publishedAt line so we can
  // append a sibling lastVerifiedAt right after.
  const guideRe = /(slug:\s*['"`]([^'"`]+)['"`][\s\S]*?publishedAt:\s*'(\d{4}-\d{2}-\d{2})',)(\s*\n\s*lastVerifiedAt:)?/g;
  const updates = [];
  let m;
  while ((m = guideRe.exec(src)) !== null) {
    const slug = m[2];
    const hasVerified = !!m[4];
    if (hasVerified) continue;
    if (verified.has(slug)) continue;
    updates.push({ slug, matchStart: m.index, matchEnd: m.index + m[1].length });
    if (updates.length >= FRESHNESS_BATCH_SIZE) break;
  }
  if (updates.length === 0) {
    log('Freshness: no guides need stamping.');
    return { noop: true };
  }
  log(`Freshness: stamping ${updates.length} guide(s) with lastVerifiedAt=${today}`);
  if (DRY) return { noop: false, dry: true, count: updates.length };
  // Apply edits from end to start so offsets remain valid.
  let next = src;
  for (const u of [...updates].reverse()) {
    const insert = `\n    lastVerifiedAt: '${today}',`;
    next = next.slice(0, u.matchEnd) + insert + next.slice(u.matchEnd);
  }
  fs.writeFileSync(GUIDES_INDEX, next);
  for (const u of updates) verified.add(u.slug);
  state.verifiedSlugs = [...verified];
  return { noop: false, count: updates.length, slugs: updates.map((u) => u.slug) };
}

function newGuideViaCreativeLoop() {
  log('Delegating to creative-loop.mjs --no-push');
  const r = run(['node', 'scripts/creative-loop.mjs', '--cycles=1', '--topics=1', '--no-push']);
  log(r.stdout);
  if (r.stderr) log('stderr:', r.stderr);
  if (!r.ok) return { noop: true, error: 'creative-loop-failed' };
  // creative-loop commits internally. Detect if anything actually changed.
  const status = run(['git', 'log', '-1', '--pretty=%s']);
  const lastMsg = status.stdout;
  const isOurs = /^feat\(content\): batch \d+/.test(lastMsg);
  if (!isOurs) return { noop: true };
  // Soften: undo the commit so the workflow can re-commit with our PR-friendly message.
  run(['git', 'reset', '--soft', 'HEAD~1']);
  return { noop: false, message: lastMsg };
}

function pickAction(state) {
  if (FORCED_ACTION) return FORCED_ACTION;
  const idx = state.cycleCount % ACTIONS.length;
  return ACTIONS[idx];
}

function main() {
  const state = readState();
  const action = pickAction(state);
  log(`cycle=${state.cycleCount + 1} action=${action} dry=${DRY}`);
  let result;
  switch (action) {
    case 'guide':     result = newGuideViaCreativeLoop(); break;
    case 'freshness': result = freshnessPass(state); break;
    case 'alert':     result = writeSeedBatch('scam-alert', SCAM_ALERTS, state, 'usedAlertSlugs'); break;
    case 'tip':       result = writeSeedBatch('weekly-tip', WEEKLY_TIPS, state, 'usedTipSlugs'); break;
    default: throw new Error(`unknown action: ${action}`);
  }

  state.cycleCount += 1;
  state.lastAction = action;
  state.lastRun = new Date().toISOString();
  writeState(state);

  if (result.noop) {
    log('No-op cycle. Exiting with code 0.');
    // Signal to workflow via env file
    if (process.env.GITHUB_OUTPUT) {
      fs.appendFileSync(process.env.GITHUB_OUTPUT, 'changed=false\n');
    }
    return;
  }

  log('Cycle produced changes.');
  if (process.env.GITHUB_OUTPUT) {
    fs.appendFileSync(process.env.GITHUB_OUTPUT, 'changed=true\n');
    fs.appendFileSync(process.env.GITHUB_OUTPUT, `action=${action}\n`);
  }
}

main();
